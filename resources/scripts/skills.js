function levelFive(){
    let x = document.querySelectorAll('._5, ._10, ._15, ._20, ._25');
    for (i = 0; i < x.length; i++) {
        x[i].style.display = "none";
    }
    let y = document.querySelectorAll('._5');
    for (i=0 ; i < x.length; i++){
        y[i].style.display ='block';
    }
}
